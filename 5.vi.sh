#vi 편집기로 파일 열기
vi 파일명

#vi 편집기 종료(명령모드)
:wq : 저장 후 종료
:q : 저장하지않고 종료

#입력모드 전환
a : 다음 커서 부터 입력
i : 현재 커서 에서 입력
o : 다음줄 부터 입력

#명령모드 전환
esc
x : 한 칸 삭제
dd : 한 줄 삭제
yy : 한 줄 복사
p : 다음 줄에 붙여넣기
G : 맨끝으로 이동
gg : 맨앞으로 이동
ctrl + f : 한 페이지 아래로 이동
ctrl + b : 한 페이지 위로 이동 

#검색
/검색하고자 하는 문자열 -> 엔터 -> n 으로 다음결과로 이동 
